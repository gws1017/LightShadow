void CMultiSpriteObjectsShader::Render(ID3D12GraphicsCommandList* pd3dCommandList, CCamera* pCamera)
{
	if (m_bActive)
	{
		XMFLOAT3 xmf3CameraPosition = pCamera->GetPosition();
		CPlayer* pPlayer = m_pPlayer;
		XMFLOAT3 xmf3PlayerPosition = pPlayer->GetPosition();
		XMFLOAT3 xmf3PlayerLook = pPlayer->GetLookVector();
		xmf3PlayerPosition.y += 5.0f;
		XMFLOAT3 xmf3Position = m_xmf3effectPosition;//Vector3::Add(xmf3PlayerPosition, Vector3::ScalarProduct(xmf3PlayerLook, 50.0f, false));
		for (int j = 0; j < m_nObjects; j++)
		{
			if (m_ppObjects[j]->m_bActive)
			{
				if (((CMultiSpriteObject*)m_ppObjects[j])->m_fOTime >= ANIM_QUIT)
				{
					m_ppObjects[j]->m_bActive = m_bActive = false; //재생완료했으니 꺼준다
					((CMultiSpriteObject*)m_ppObjects[j])->m_fOTime = 0.0f; // 초기화
					break;
				}
			}
			
			if (m_ppObjects[j])
			{
				m_ppObjects[j]->m_bActive = m_bActive;
				m_ppObjects[j]->SetPosition(xmf3Position);
				m_ppObjects[j]->SetLookAt(xmf3CameraPosition, XMFLOAT3(0.0f, 1.0f, 0.0f));
			}
		}
		UpdateShaderVariables(pd3dCommandList);
		//CObjectsShader::Render(pd3dCommandList, pCamera);
		if (m_pd3dGraphicsRootSignature) pd3dCommandList->SetGraphicsRootSignature(m_pd3dGraphicsRootSignature);
		if (m_ppd3dPipelineStates) pd3dCommandList->SetPipelineState(m_ppd3dPipelineStates[0]);
		if (m_pd3dCbvSrvDescriptorHeap)pd3dCommandList->SetDescriptorHeaps(1, &m_pd3dCbvSrvDescriptorHeap);

		for (int j = 0; j < m_nObjects; j++)
		{
			if (m_ppObjects[j]) m_ppObjects[j]->Render(pd3dCommandList, pCamera);
		}
	}
}

